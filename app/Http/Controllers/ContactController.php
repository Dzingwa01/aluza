                ->send(new General($contact));
            return response()->json(['message'=>'Your Message has been submitted successfully. We will respond to you as soon as possible.'],200);
        }catch(\Exception $e){
            DB::rollBack();
            return response()->json(['message'=>'An error occurred, please contact us telephonically.'],400);
        }
    }

